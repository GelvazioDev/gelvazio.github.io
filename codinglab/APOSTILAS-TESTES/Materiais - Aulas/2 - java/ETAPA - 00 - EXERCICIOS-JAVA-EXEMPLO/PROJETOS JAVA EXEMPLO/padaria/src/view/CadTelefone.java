private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed

        TelefoneDB telefonedb = new TelefoneDB();
        telefonedb.setModel(telefonedb.getComboTelefone());
        
        ArrayList<Telefone> listaTelefone = telefonedb.getTodos();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("C贸digo");
        modelo.addColumn("C贸digo Cliente");
        modelo.addColumn("Numero");
        modelo.addColumn("Tipo");
        
        for (Telefone auxtelefone : listaTelefone){            
            Object[] objetos = {auxtelefone.getTel_sequencia(),auxtelefone.getCli_codigo(),auxtelefone.getNumero(),auxtelefone.getTipo()};
            modelo.addRow(objetos);
            JOptionPane.showMessageDialog(null, "C贸digo: "+auxtelefone.getCli_codigo());
        }
        tbGrid.setModel(modelo);
        int linhas = tbGrid.getRowCount();
        if (linhas > 0){
            tbGrid.setRowSelectionInterval(0, 1);
            Object auxCodigo = tbGrid.getValueAt(0, 2);
            Object auxCliCodigo= tbGrid.getValueAt(0, 3);
            Object auxNumero = tbGrid.getValueAt(0, 4);
            Object auxTipo = tbGrid.getValueAt(0, 5);
            edtCodigo.setText(auxCodigo.toString());
            edtCliCodigo.setText(auxCliCodigo.toString());
            edtNumero.setText(auxNumero.toString());
            edtTipo.setText(auxTipo.toString());
       
        } 
    

}//GEN-LAST:event_btnAtualizarActionPerformed

