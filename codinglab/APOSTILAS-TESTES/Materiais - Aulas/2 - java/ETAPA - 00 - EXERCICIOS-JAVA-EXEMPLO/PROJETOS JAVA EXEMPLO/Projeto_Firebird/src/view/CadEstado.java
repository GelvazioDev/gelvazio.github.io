    private void BtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnExcluirActionPerformed
        // TODO add your handling code here:
        int resposta =  JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o registro?");
        if (resposta == JOptionPane.YES_OPTION){
            EstadoDB estadodb = new EstadoDB();
            String auxSigla = EdtSigla.getText();
            if (estadodb.excluirEstado(auxSigla)){
               JOptionPane.showMessageDialog(null,"Exclusão efetuada com sucesso!"); 
            }
            //JOptionPane.showMessageDialog(null,"Clicou em sim");
        }
        atualizarTela(false);
    }//GEN-LAST:event_BtnExcluirActionPerformed

    private void BtnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGravarActionPerformed
        // TODO add your handling code here:                
        EstadoDB estadodb = new EstadoDB();
        String auxSigla = EdtSigla.getText();
        String auxNome = EdtNome.getText();        
        if (estadodb.getEstado(auxSigla)){
           //Alteração                       
           Estado estado = new Estado(auxSigla,auxNome);
           if (estadodb.alterarEstado(estado)){
              JOptionPane.showMessageDialog(null,"Alteração realizada com sucesso!"); 
           }else{
              JOptionPane.showMessageDialog(null,"Não foi possível efetuar a alteração!"); 
           }           
           atualizarTela(false);
        }else{
           //Inclusão             
           Estado estado = new Estado(auxSigla,auxNome);
           if (estadodb.inserirEstado(estado)){
              JOptionPane.showMessageDialog(null,"Inclusão realizada com sucesso!"); 
           }else{
              JOptionPane.showMessageDialog(null,"Não foi possível efetuar a inclusão!"); 
           } 
           atualizarTela(false);
        }
    }//GEN-LAST:event_BtnGravarActionPerformed

