    private void atualizarTela(boolean verificaConsulta){
        JComboBox combo = new JComboBox();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Sigla");
        modelo.addColumn("Nome");        
        EstadoDB estadodb = new EstadoDB();
        ArrayList<Estado> estados = new ArrayList();
        if (verificaConsulta == true){
            estados = estadodb.getConsulta(edtBusca.getText());
        }else{
        estados = estadodb.getTodos();
        }
        for (Estado auxEstado : estados){
            modelo.addRow(new Object[] {auxEstado.getEst_sigla(),auxEstado.getNome()});            
            combo.addItem(auxEstado);
        }        
        TbGrid.setModel(modelo);
        int registros = TbGrid.getRowCount();
        if (registros > 0){
            TbGrid.setRowSelectionInterval(0, 0);
            Object auxSigla = TbGrid.getValueAt(0, 0);
            Object auxNome = TbGrid.getValueAt(0, 1);            
            EdtSigla.setText(auxSigla.toString());
            EdtNome.setText(auxNome.toString());  
        }     
        
    }
    
