    private void BtnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAnteriorActionPerformed
        // TODO add your handling code here:
        int registros = TbGrid.getRowCount();
        if (registros > 0){
            int linhaSel = TbGrid.getSelectedRow();            
            int linha = linhaSel - 1;
            if (linha>=0){
                TbGrid.setRowSelectionInterval(linha, linha);
                Object auxSigla = TbGrid.getValueAt(linha, 0);
                Object auxNome = TbGrid.getValueAt(linha, 1);                
                EdtSigla.setText(auxSigla.toString());
                EdtNome.setText(auxNome.toString());  
            }
        }
    }//GEN-LAST:event_BtnAnteriorActionPerformed

